function speakContentDrop(){
    var speakDrop = document.getElementById('drop_this');
    if (speakDrop.style.display == 'none') {
        speakDrop.style.display = 'block';
    } 
    else {
        speakDrop.style.display = 'none';
    }
}

function menuContentDrop(){
    let menuDrop = document.getElementById('drop_this_too');
    menuDrop.style.display = 'block';
}

function closeMenu(){
    let exitMenu = document.getElementById('drop_this_too');
    exitMenu.style.display = 'none';
}

